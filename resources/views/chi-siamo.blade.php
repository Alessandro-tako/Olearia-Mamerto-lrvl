        <article>
            <h3 class="article-title">Il nostro metodo</h3>
            <p>
                La produzione del nostro EVOO ha inizio nel mese di ottobre con la raccolta dei frutti: questa fase è
                condotta in modo manuale, con l'ausilio di pettini pneumatici, che facilitano il distacco del frutto
                dalla pianta in maniera non traumatica.
                <br>
                Successivamente, e sempre nell'arco di 8 ore dalla raccolta, le olive vengono trasferite al frantoio a
                ciclo continuo per la molitura.
                <br>
                Questa è eseguita mantenendo bassi i livelli di temperatura, in modo da preservare l'integrità delle
                sostanze organolettiche specifiche del prodotto.
                <br>
                Ultimata la fase della molitura, l'EVOO viene filtrato e trasferito all'interno di apposite cisterne in
                acciaio inox con colmatura in azoto e stoccato in ambienti a temperatura controllata.
                <br>
                La colmatura in azoto inibisce il processo di ossidazione dell'EVOO, mentre la temperatura controllata e
                costante ne garantisce la conservazione ottimale.
            </p>
        </article>
